# Simple Python Program: Addition of two numbers
# Purpose: Learning basic input and output

print("--- Welcome to your Personal AI Assistant's First Code ---")

# Step 1: Taking input from the user
num1 = input("Enter your first number: ")
num2 = input("Enter your second number: ")

# Step 2: Converting text (string) to numbers (integer) and adding them
sum_result = int(num1) + int(num2)

# Step 3: Displaying the final result
print("The sum of the two numbers is:", sum_result)

print("Process completed successfully.")
